String pickColor() {
    String letters[16] = {"a", "b", "c", "d", "e", "f", "1", "2", "3", "4", "5", "6", "7", "8", "9", "0"};
    String randString = "";
    for (int i = 1; i <= 6; i++) {
        randString += letters[random(0, 15)];
    }
    return "#" + randString;
}

void drawLetter(char letter, int startX, int startY, String color) {
  int x_ = (letter - 'a') * 4;
  for (int x = 1; x <= 3; x++) {
    for (int y = 1; y <= 5; y++) {
      if (alphabet[y-1][x-1+x_] == 1) {
        pixel(x+startX, 33-y-startY, color);
      }
    }
  }
}

void drawNumbers(int number, int startX, int startY, String color) {
  int x_ = number * 4;
  for (int x = 1; x <= 3; x++) {
    for (int y = 1; y <= 5; y++) {
      if (numbers[y-1][x-1+x_] == 1) {
        pixel(x+startX, 33-y-startY, color);
      }
    }
  }
}


void write(const char* word, int y) {
    int x = 0; // Posição inicial no eixo X

    String color=pickColor();

    // Itera por cada caractere da palavra
    for (int i = 0; word[i] != '\0'; i++) {
        char c = word[i];
        if (c >= 'a' && c <= 'z') { // Se for uma letra minúscula
            drawLetter(c, x, y, color);
        } else if (c >= '0' && c <= '9') { // Se for um número
            drawNumbers(c - '0', x, y, color); // Converte o caractere numérico para inteiro
        } else {
            // Espaços ou caracteres não suportados podem apenas incrementar a posição X
            x += 4; // Define o espaço entre caracteres desconhecidos
        }
        x += 4; // Incrementa a posição X para o próximo caractere
    }
}

